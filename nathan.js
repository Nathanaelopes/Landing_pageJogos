function sinopse(titulo){
    const jogo = dadosJogos[titulo];
    
    // Verifica se o jogo existe no banco de dados
    if (!jogo) {
        console.error(`Jogo "${titulo}" não encontrado.`);
        return;
    }

    document.getElementById("titulo").innerHTML = titulo;
    document.getElementById("texto").innerHTML = jogo.texto;
    document.getElementById("img").src = jogo.img;

    document.getElementById("sinopse").style.display="block";
    document.getElementById("overlay").style.display="block";
}

